module Utils.Runner (
  Run (..),
  Named (..),
) where

import GHC.TypeLits (Symbol)

class
  Monad m =>
  Run runner m message result
    | runner -> m
    , runner message -> result
  where
  run :: runner -> message -> m result

newtype Named (name :: Symbol) a = Named a
